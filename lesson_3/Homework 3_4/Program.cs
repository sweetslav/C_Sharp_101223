// **(не обязательно): Дано натуральное число в диапазоне от 1 до 100 000. Создайте массив, состоящий из цифр этого числа. Старший разряд числа должен располагаться на 0-м индексе массива, младший – на последнем. Размер массива должен быть равен количеству цифр.

int number = 81673; // Пример натурального числа

// Преобразуем число в строку для подсчета количества цифр
string numberAsString = number.ToString();

// Создаем массив целых чисел размером, равным количеству цифр в числе
int[] digitArray = new int[numberAsString.Length];

// Заполняем массив цифрами числа, начиная с последнего (младшего разряда)
for (int i = numberAsString.Length - 1; i >= 0; i--)
{
    digitArray[numberAsString.Length - 1 - i] = int.Parse(numberAsString[i].ToString());
}

// Выводим массив на консоль для проверки
Console.WriteLine("Массив цифр числа:");
foreach (int digit in digitArray)
{
    Console.Write($"{digit} ");
}

